    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(aux==0){
           Cliente objCliente = new Cliente();
        
           objCliente.setNome(txtNome.getText());
           objCliente.setTipoCliente((EnumCliente) cbTipo.getSelectedItem());
           objCliente.setCPF(txtCpf.getText());
        
           objBanco.adicionarCliente(objCliente);
        
           mostrarTelaClientes(objBanco.getListaclientes());
           limparCampos();
        }
        else{ 
           c.setNome(txtNome.getText());
           c.setTipoCliente((EnumCliente) cbTipo.getSelectedItem());
           c.setCPF(txtCpf.getText());
        
          //objBanco.adicionarCliente(c);
        
          mostrarTelaClientes(objBanco.getListaclientes());
          aux=0;
          limparCampos();
          btnCadastrar.setText("Cadastrar");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

