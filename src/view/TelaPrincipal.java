    private void mostrarTelaClientes(List<Cliente> contatosQueQueroMostrar) {
        DefaultListModel modelo = new DefaultListModel();
        for (Cliente cliente : contatosQueQueroMostrar) {
            modelo.addElement(cliente);
        }
        lstClientes.setModel(modelo);
    }
    
